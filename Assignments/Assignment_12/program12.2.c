int count_factors(int number)
{
    int i =0, iCount = 0;
    
    if(number < 0)
    {
        printf("Invalid Number\n");
    }
    for(i=1; i< number; i++)
    {
        if((number % i)==0)
        {
            iCount++;
        }
    }
    return iCount;
}//End of count_factors

