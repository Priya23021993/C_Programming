#include<stdio.h>                               //for input output
#include<stdbool.h>                             //For bool datatype

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  is_divisible_by_five
//  Description:    It is use to check if number is divisible by 5
//  Input:          integer
//  Output:         boolean
//  Author:         Priyanka Vikas Bhor
//  Date:           26/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

bool is_divisible_by_five(int number)
{
    if((number % 5)==0)
    {
        return true;
    }
    else
    {
        return false;
    }
}//End of is_divisible_by_five

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter Number:");
    scanf("%d",&number);

    printf("%s\n", is_divisible_by_five(number)? "Yes":"No");
    return 0;
}//End of main

