#include<stdio.h>                   //for input output

////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  MultipleDisplay
//  Description:    It is use to accept N from and print first 5 multiples of N
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           21/10/2025
//
///////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{
    int i = 0;

    for(i=1; i <=5; i++)
    {
        printf("%d\t", iNo * i);
    }
}  //End of MultipleDisply

//////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
/////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;                      //to accept input

   printf("Enter Number:");
   scanf("%d",&iValue);

   MultipleDisplay(iValue);            //function call
   return 0;
}  ///End of main

