#include<stdio.h>                         //For input output

//////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  Display
//  Description:    It is use to accept number from user & print its number line
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           21/10/2025
//
/////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int i=0;
    
    for(i= -iNo; i <= iNo; i++)
    {
        printf("%d\t",i);
    }

}///End of display

////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
///////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                    //To accept input

    printf("Enter Number:");
    scanf("%d",&iValue);

    Display(iValue);                 //Function call

    return 0;
}///End of main

