////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  OddDisplay
//  Description:    It is use to accept N from user and print all odd numbers upto N
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           21/10/2025
//
///////////////////////////////////////////////////////////////////////////////////////

void OddDisplay(int iNo)
{
    int i =1;
    for(i=1; i<= iNo; i++)
    {
        if((i % 2)!=0)
        {
            printf("%d\t",i);
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
/////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue =0;                         //To accept input

    printf("Enter Number:");
    scanf("%d",&iValue);

    OddDisplay(iValue);                   //Function call

    return 0;
}///End of main

