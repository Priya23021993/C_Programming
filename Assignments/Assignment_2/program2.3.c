////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  Display
//  Description:    It is use to print if number is less than 10 then print "Hello" otherwise print "Demo"
//  Input:          integer
//  Output:         character
//  Author:         Priyanka Vikas Bhor
//  Date:           16/10/2025
