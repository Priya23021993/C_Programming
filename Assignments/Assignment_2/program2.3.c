////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
     if(iNo < 10)
    {
        printf("Hello\n");
    }
    else
    {
        printf("Demo\n");
    }
}///End of Display

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue =0;                               //to accept input

    printf("Enter Number:");
    scanf("%d",&iValue);

    Display(iValue);                             //function call
    return 0;
}///end of main