#include<stdio.h>                                   //for input output

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  sum_even_numbers
//  Description:    It is use to find sum of first N even numbers
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           27/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

int sum_even_numbers(int limit)
{
    int iSum = 0;
    for(int i= 1; i <= limit ; i++)
    {
        iSum = iSum + i;                                //Business logic
    }
    return iSum;
    
}//end of sum_even_numbers

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;                                       // to accept input

    printf("Enter number:");
    scanf("%d",&limit);

    printf("%d\n",sum_even_numbers(limit));
    return 0;
}//end of main


