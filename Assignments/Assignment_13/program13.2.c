#include<stdio.h>                                       //for input output

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  print_even_number
//  Description:    It is use to print all even numbers upto N
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           27/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void print_even_numbers(int limit)
{
    int i=0;
    for(i=2; i<= limit ;i+=2)
    {
        printf("%d\n",i);
    }
}//end of print_even_number

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;                                     //To accept input

    printf("Enter number:");
    scanf("%d",&limit);

    print_even_numbers(limit);
    return 0;
}//end of main


