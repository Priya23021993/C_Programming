#include<stdio.h>                                      //For input output

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  print_number
//  Description:    It is use to print all numbers from 1 to N
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           27/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void print_numbers(int limit)
{
    int i=0;
    for(i=1; i<= limit ;i++)
    {
        printf("%d\n",i);
    }
}//end of print_numbers

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;                                     //to accept input

    printf("Enter number:");
    scanf("%d",&limit);

    print_numbers(limit);
    return 0;
}//end of main

