#include<stdio.h>                                  //for input output

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  print_odd_number
//  Description:    It is use to print all odd numbers upto N
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           27/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

void print_odd_numbers(int limit)
{
    int i=0;
    for(i=1; i<= limit ;i+=2)
    {
        printf("%d\n",i);
    }
}//end print_odd_numbers

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;                                                 //to accept input

    printf("Enter number:");
    scanf("%d",&limit);

    print_odd_numbers(limit);
    return 0;
}//end of main

