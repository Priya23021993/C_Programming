#include<stdio.h>                                         //for input output

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  sum_natural_numbers
//  Description:    It is use to find sum of first N natural numbers
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           27/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////

int sum_natural_numbers(int limit)
{
    int i=0, iSum = 0;
    for(i=1; i<= limit ;i++)
    {
        iSum = iSum + i;                                 //Business logic
    }
    return iSum;
}//end of sum_natural_numbers

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;                                             //to accept input

    printf("Enter number:");
    scanf("%d",&limit);

    printf("%d\n",sum_natural_numbers(limit));
    return 0;
}

