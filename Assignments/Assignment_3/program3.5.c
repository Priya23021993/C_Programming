//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';                                   //To accept input
    BOOL bRet = FALSE;                                    //To stoare Result

    printf("enter Character\n");
    scanf("%c",&cValue);

    bRet = ChkVowel(cValue);                              //Function call

    if(bRet == TRUE)
    {
        printf("It Is Vowel");
    }
    else
    {
 
        printf("It is not Vowel");
    }

    return 0;
}///End of main

