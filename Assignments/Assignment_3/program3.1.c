void PrintEven(int iNo)
{
    if(iNo <= 0)
    {
        return;
    }
     int iCnt = 0;
     int iEven = 2;
     while(iCnt < iNo)
    {
       printf("%d\n",iEven);
       iEven += 2;
       iCnt++;
    }

}///End of PrintEven

