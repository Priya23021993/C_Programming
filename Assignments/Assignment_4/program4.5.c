//////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  FactDiff
//  Description:    It is use to return difference between summation of all its factors and non factors
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           18/10/2025
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////

int FactDiff(int iNo)
{
    int iCnt = 0;
    int SumFact =0;
    int SumNonFact = 0;

    for(iCnt = 1; iCnt < iNo; iCnt++)
    {
        if((iNo % iCnt)==0)
        {
            SumFact = SumFact + iCnt;
        }
        else
        {
            SumNonFact = SumNonFact + iCnt;
        }
    }
    return SumFact - SumNonFact;
}  //End of FactDiff

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;               //To accept Input
    int iRet = 0;                 // to store result

    printf("Enter Number");
    scanf("%d",&iValue);

    iRet = FactDiff(iValue);        //Function call

    printf("%d",iRet);

    return 0;
} ///End of main

