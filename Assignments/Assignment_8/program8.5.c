//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  TableRev
//  Description:    It is use to accept number from user and display its table in reverse
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           22/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////

void TableRev (int iNo)
{
    int iCnt = 0;
    if(iNo < 0)                                  //Updater
    {
        iNo = -iNo;
    }

    for(iCnt = 10; iCnt >= 1; iCnt--)
    {
        printf("%d\t",iNo * iCnt);
    }

}//End Of TableRev

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                    //To accept input

    printf("Enter Number:");
    scanf("%d",&iValue);

    TableRev(iValue);                 //Function call

    return 0;
}  //End of main

