#include<stdio.h>                              //For input output

////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  Table
//  Description:    It is use to accept number from user and display its table
//  Input:          integer
//  Output:         integer
//  Author:         Priyanka Vikas Bhor
//  Date:           22/10/2025
//
///////////////////////////////////////////////////////////////////////////////////////


void Table (int iNo)
{
    int iCnt = 0;
    if(iNo < 0)                             //Updater
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= 10; iCnt++)
    {
        printf("%d\t",iNo * iCnt);
    }

}//End of Table

////////////////////////////////////////////////////////////////////////////////////////
//
//  Entery point function for the Application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                             //To accept input

    printf("Enter Number:");
    scanf("%d",&iValue);

    Table(iValue);                              //Function call
}//End of main

